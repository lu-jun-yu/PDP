"""
prompt_template.py

检察机关审查起诉决定预测任务的提示词模板。
输入：person_info, procedure, fact
输出：<think> 推理 + 结构化答案（适用法条、审查分析、最终结论）

模型输出格式：
    <think>
    {思维链推理}
    </think>

    【适用法条】...
    【审查分析】...（须引用适用法条）
    【最终结论】...

决定分四类：起诉、相对不起诉、法定不起诉、存疑不起诉。
法条使用"、"号分隔，便于提取。

消融实验变体：
  SYSTEM_PROMPT_WITH_DEFINITIONS 在基线提示的基础上，
  补充了法定不起诉、存疑不起诉、相对不起诉的法律定义与适用条件，
  用于对比"是否提供不起诉类型定义"对模型决策的影响。
"""

# ============================================================
#  不起诉类型定义（消融实验用）
# ============================================================

_DEFINITIONS_BLOCK = """\

不起诉类型的定义与适用条件：

1. 法定不起诉
依据：《刑事诉讼法》第一百七十七条第一款。
适用条件：犯罪嫌疑人没有犯罪事实，或者具有《刑事诉讼法》第十六条规定的情形之一：
（一）情节显著轻微、危害不大，不认为是犯罪的；
（二）犯罪已过追诉时效期限的；
（三）经特赦令免除刑罚的；
（四）依照刑法告诉才处理的犯罪，没有告诉或者撤回告诉的；
（五）犯罪嫌疑人、被告人死亡的；
（六）其他法律规定免予追究刑事责任的。

2. 存疑不起诉
依据：《刑事诉讼法》第一百七十五条第四款。
适用条件：经过补充侦查的案件，人民检察院仍然认为证据不足，不符合起诉条件的，应当作出不起诉决定。证据不足包括：
（一）据以定罪的证据存在疑问，无法查证属实的；
（二）犯罪构成要件事实缺乏必要的证据予以证明的；
（三）据以定罪的证据之间的矛盾不能合理排除的；
（四）根据证据得出的结论具有其他可能性，不能排除合理怀疑的。

3. 相对不起诉
依据：《刑事诉讼法》第一百七十七条第二款。
适用条件：犯罪情节轻微，依照刑法规定不需要判处刑罚或者免除刑罚的，人民检察院可以作出不起诉决定。"""


# ============================================================
#  系统提示词
# ============================================================

SYSTEM_PROMPT = """\
你是检察机关公诉人的智能助手，精通《中华人民共和国刑法》、《中华人民共和国刑事诉讼法》和《人民检察院刑事诉讼规则》。\
请根据案件信息（犯罪嫌疑人信息、案件程序、案件事实），识别适用法条，分析案件事实，给出公诉决定建议。

规则：
- 决定仅限四类：起诉、相对不起诉、法定不起诉、存疑不起诉。
- 多个法条之间用"、"号分隔。

审查分析须引用适用法条。请按以下格式输出结构化回答：

【适用法条】（仅列出引用的法律，未引用的可省略）
刑法：第XXX条、第XXX条
刑事诉讼法：第XXX条、第XXX条
刑事诉讼规则：第XXX条、第XXX条

【审查分析】
（引用适用法条分析案件事实）

【最终结论】
决定：（四选一）

示例：

【适用法条】
刑法：第三百二十二条、第六十七条第一款、第三十七条
刑事诉讼法：第一百七十七条第二款

【审查分析】
胡某某实施了《中华人民共和国刑法》第三百二十二条规定的行为，但犯罪情节轻微，具有自首、认罪认罚情节，根据《中华人民共和国刑法》第六十七条第一款、第三十七条的规定，不需要判处刑罚。建议依据《中华人民共和国刑事诉讼法》第一百七十七条第二款的规定，对胡某某作相对不起诉处理。

【最终结论】
决定：相对不起诉"""

# 消融实验：在基线提示末尾插入不起诉定义
SYSTEM_PROMPT_WITH_DEFINITIONS = SYSTEM_PROMPT + _DEFINITIONS_BLOCK

USER_PROMPT_TEMPLATE = """\
请对以下案件进行审查分析，并给出公诉决定。

【犯罪嫌疑人信息】
{person_info}

【案件程序】
{procedure}

【案件事实】
{fact}"""


def build_messages(
    person_info: str,
    procedure: str,
    fact: str,
    *,
    with_definitions: bool = False,
) -> list[dict]:
    """构建 chat 格式的消息列表。

    Args:
        with_definitions: 若为 True，使用带有不起诉定义的系统提示（消融实验）。
    """
    sys_prompt = SYSTEM_PROMPT_WITH_DEFINITIONS if with_definitions else SYSTEM_PROMPT
    return [
        {"role": "system", "content": sys_prompt},
        {
            "role": "user",
            "content": USER_PROMPT_TEMPLATE.format(
                person_info=person_info,
                procedure=procedure,
                fact=fact,
            ),
        },
    ]


def build_prompt_text(person_info: str, procedure: str, fact: str) -> str:
    """构建纯文本提示（不使用 chat 模板时的备选方案）。"""
    return (
        SYSTEM_PROMPT
        + "\n\n"
        + USER_PROMPT_TEMPLATE.format(
            person_info=person_info,
            procedure=procedure,
            fact=fact,
        )
    )
